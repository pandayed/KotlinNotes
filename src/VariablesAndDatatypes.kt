fun varAndVal(){

    //ctrl+shift+p on identifier shows its datatype

    //var can be reassigned
    var num = 10
    println(num)
    num = 20
    println(num)

    //val can not be reassigned
    val myString = "Hello"
    println(myString)

    //val or var both need not be initialized, but for that we need to specify datatype
}

